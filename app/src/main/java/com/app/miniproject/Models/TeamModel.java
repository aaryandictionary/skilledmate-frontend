package com.app.miniproject.Models;

public class TeamModel {
    private String teamImage,teamTitle,teamDescription,teamStatus,teamRequirement;

    public String getTeamImage() {
        return teamImage;
    }

    public void setTeamImage(String teamImage) {
        this.teamImage = teamImage;
    }

    public String getTeamTitle() {
        return teamTitle;
    }

    public void setTeamTitle(String teamTitle) {
        this.teamTitle = teamTitle;
    }

    public String getTeamDescription() {
        return teamDescription;
    }

    public void setTeamDescription(String teamDescription) {
        this.teamDescription = teamDescription;
    }

    public String getTeamStatus() {
        return teamStatus;
    }

    public void setTeamStatus(String teamStatus) {
        this.teamStatus = teamStatus;
    }

    public String getTeamRequirement() {
        return teamRequirement;
    }

    public void setTeamRequirement(String teamRequirement) {
        this.teamRequirement = teamRequirement;
    }
}
